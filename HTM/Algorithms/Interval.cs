using HTM.Models;

namespace HTM.Algorithms
{
    internal class Interval
    {
        private int x1;
        private int x2;
        private int y1;
        private int y2;        

        public Interval(int x1, int x2, int y1, int y2)
        {
            this.x1 = x1;
            this.x2 = x2;
            this.y1 = y1;
            this.y2 = y2;
        }                               
    }
}
